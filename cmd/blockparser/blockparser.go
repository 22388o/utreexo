func loadDb() error {

	// open database
	o := new(opt.Options)
	o.ReadOnly = true
	lvdb, err := leveldb.OpenFile("./index", o)
	if err != nil {
		panic(err)
	}
	defer lvdb.Close()

	//lvdb.Get()

	return nil
}
